#!/usr/bin/env node
// Generates a compact Mermaid projection of the repository topology contract.
// Authority: docs/contracts/repository-topology.md

import { writeFile } from 'node:fs/promises';
import { join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = fileURLToPath(new URL('..', import.meta.url));
const outputPath = join(root, 'docs/generated/dependency-architecture-contract.mmd');

const nodes = [
  ['Docs', 'docs/contracts'],
  ['NoteModel', 'contexts/note-model'],
  ['Scheduler', 'contexts/scheduler'],
  ['ContextAssembly', 'contexts/context-assembly'],
  ['Memory', 'contexts/memory'],
  ['AiOperations', 'contexts/ai-operations'],
  ['Topology', 'contexts/topology'],
  ['Web', 'apps/web'],
  ['Worker', 'apps/worker'],
  ['NoteSotWriteAdapters', 'apps/worker/src/note-model write adapters'],
  ['GeneratedApi', 'apps/workspace-api/generated'],
  ['GeneratedDocs', 'docs/generated'],
];

const edges = [
  ['Docs', 'NoteModel', 'authority'],
  ['Docs', 'Scheduler', 'authority'],
  ['Docs', 'ContextAssembly', 'authority'],
  ['Docs', 'Memory', 'authority'],
  ['Docs', 'AiOperations', 'authority'],
  ['Docs', 'Topology', 'authority'],
  ['Docs', 'GeneratedApi', 'projection'],
  ['Docs', 'GeneratedDocs', 'projection'],
  ['Web', 'NoteModel', 'contract import'],
  ['Web', 'Scheduler', 'contract import'],
  ['Web', 'ContextAssembly', 'contract import'],
  ['Web', 'Memory', 'contract import'],
  ['Web', 'AiOperations', 'contract import'],
  ['Web', 'Worker', 'API only'],
  ['Worker', 'NoteModel', 'contract import'],
  ['Worker', 'Scheduler', 'contract import'],
  ['Worker', 'ContextAssembly', 'contract import'],
  ['Worker', 'Memory', 'contract import'],
  ['Worker', 'AiOperations', 'contract import'],
  ['Scheduler', 'NoteModel', 'section snapshots'],
  ['ContextAssembly', 'NoteModel', 'note-card semantics'],
  ['ContextAssembly', 'Memory', 'eligible memory'],
  ['AiOperations', 'NoteModel', 'block vocabulary'],
  ['AiOperations', 'Memory', 'memory vocabulary'],
];

const forbiddenEdges = [
  ['NoteModel', 'Web', 'forbidden'],
  ['NoteModel', 'Worker', 'forbidden'],
  ['Scheduler', 'Worker', 'forbidden'],
  ['ContextAssembly', 'Worker', 'forbidden'],
  ['Memory', 'Worker', 'forbidden'],
  ['AiOperations', 'Worker', 'forbidden'],
  ['Worker', 'Web', 'forbidden'],
  ['Web', 'GeneratedApi', 'forbidden'],
  ['AiOperations', 'NoteSotWriteAdapters', 'no direct SoT write edge'],
];

function nodeLine([id, label]) {
  return `  ${id}["${label}"]`;
}

function edgeLine([from, to, label]) {
  return `  ${from} -->|"${label}"| ${to}`;
}

function forbiddenEdgeLine([from, to, label]) {
  return `  ${from} -. "${label}" .-> ${to}`;
}

const content = [
  '%% Generated by scripts/generate-dependency-architecture-contract.mjs',
  '%% Authority: docs/contracts/repository-topology.md',
  'flowchart LR',
  ...nodes.map(nodeLine),
  ...edges.map(edgeLine),
  ...forbiddenEdges.map(forbiddenEdgeLine),
  '',
  '  classDef contract fill:#eef6ff,stroke:#4b7bec,color:#17202a',
  '  classDef app fill:#f8f1ff,stroke:#8e44ad,color:#17202a',
  '  classDef projection fill:#f6f6f6,stroke:#7f8c8d,color:#17202a',
  '  classDef forbidden stroke:#c0392b,stroke-dasharray: 4 3,color:#c0392b',
  '  class Docs,NoteModel,Scheduler,ContextAssembly,Memory,AiOperations,Topology contract',
  '  class Web,Worker,NoteSotWriteAdapters app',
  '  class GeneratedApi,GeneratedDocs projection',
  '',
].join('\n');

await writeFile(outputPath, content, 'utf8');
console.log(`wrote ${outputPath}`);
