#!/usr/bin/env node
// Generates a compact Mermaid projection from dependency-cruiser's observed JSON graph.
// Authority: .dependency-cruiser.cjs, docs/contracts/repository-topology.md

import { readFile, writeFile } from 'node:fs/promises';
import { join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = fileURLToPath(new URL('..', import.meta.url));
const inputPath = join(root, 'docs/generated/dependency-cruiser-observed.json');
const outputPath = join(root, 'docs/generated/dependency-cruiser-observed.mmd');

function architectureNode(filePath) {
  const match = /^(apps\/[^/]+|contexts\/[^/]+)/.exec(filePath);
  return match?.[1] ?? null;
}

function nodeId(label) {
  return label.replace(/[^A-Za-z0-9]/g, '_');
}

function edgeKey(from, to) {
  return `${from} -> ${to}`;
}

const graph = JSON.parse(await readFile(inputPath, 'utf8'));
const nodes = new Set();
const edges = new Set();

for (const module of graph.modules ?? []) {
  const from = architectureNode(module.source);
  if (from === null) {
    continue;
  }
  nodes.add(from);
  for (const dependency of module.dependencies ?? []) {
    const target = dependency.resolved ?? dependency.module;
    const to = architectureNode(target);
    if (to === null || to === from) {
      continue;
    }
    nodes.add(to);
    edges.add(edgeKey(from, to));
  }
}

const sortedNodes = [...nodes].sort();
const sortedEdges = [...edges].sort();
const lines = [
  '%% Generated by scripts/generate-dependency-architecture-observed.mjs',
  '%% Source: docs/generated/dependency-cruiser-observed.json',
  'flowchart LR',
  ...sortedNodes.map((label) => `  ${nodeId(label)}["${label}"]`),
  ...sortedEdges.map((edge) => {
    const [from, to] = edge.split(' -> ');
    return `  ${nodeId(from)} --> ${nodeId(to)}`;
  }),
  '',
  '  classDef app fill:#f8f1ff,stroke:#8e44ad,color:#17202a',
  '  classDef context fill:#eef6ff,stroke:#4b7bec,color:#17202a',
  `  class ${sortedNodes.filter((node) => node.startsWith('apps/')).map(nodeId).join(',')} app`,
  `  class ${sortedNodes.filter((node) => node.startsWith('contexts/')).map(nodeId).join(',')} context`,
  '',
];

await writeFile(outputPath, lines.join('\n'), 'utf8');
console.log(`wrote ${outputPath}`);
